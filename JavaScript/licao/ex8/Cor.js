export function primario (cor) {
    let x = ''

    if (cor == 'vermelho') {
        x = 'A cor é primaria'
    }

    else if (cor == 'azul') {
        x = 'A cor é primaria'
    }

    else if (cor == 'amarelo') {
        x = 'A cor é primaria'
    }

    else if (cor) {
        x = 'Não é primaria'
    }
 
    return x;
}